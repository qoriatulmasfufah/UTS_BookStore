    private void selectedData() {
        String kolom[] = {"NamaKasir","TanggalBeli","IDBarang","NamaBarang","HargaBarang","JumlahBarang","Diskon","HargaTotal"};
                DefaultTableModel dtm = new DefaultTableModel(null, kolom);
                String SQL = "SELECT * FROM tb_product";
                ResultSet rs = KoneksiDB.executeQuery(SQL);
                try{
                    while(rs.next()){
                        String IDBarang     = rs.getString(1);
                        String NamaBarang   = rs.getString(2);
                        String HargaBarang  = rs.getString(3);
                        String NamaKasir    = rs.getString(4);
                        String JumlahBarang = rs.getString(5);
                        String TanggalBeli  = rs.getString(6);
                        String Diskon       = rs.getString(7);
                        String HargaTotal   = rs.getString(8);
                        String data[] = {NamaKasir,TanggalBeli,IDBarang,NamaBarang,HargaBarang,JumlahBarang,Diskon,HargaTotal};
                        dtm.addRow(data);
                    }
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(mainmenu.class.getName()).log(Level.SEVERE, null, ex);
        }
              tbData.setModel(dtm);
    }
    
