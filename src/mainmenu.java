    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        
         SimpleDateFormat dateFormat= new SimpleDateFormat("yyy-MM-dd");
         String tanggal = dateFormat.format(Tanggal.getDate());
       
        if ("".equals(tfNamaKasir.getText())||"".equals(tanggal)||"".equals(tfIDBarang.getText())||"".equals(tfNamaBarang.getText())||"".equals(tfHargaBarang.getText())||"".equals(tfJumlahBarang.getText())) {
           JOptionPane.showMessageDialog(this, "Data nyoba", "Kosong", JOptionPane.WARNING_MESSAGE);
        }else{
            
            
            int hargabarang;
            int jumlahbarang;
            int diskon = 10;
            
            String hargakotor = tfHargaBarang.getText();
            hargabarang = Integer.parseInt(hargakotor);
            
            String jumlahkotor = tfJumlahBarang.getText();
            jumlahbarang = Integer.parseInt(jumlahkotor);
            
            int totalhargabersih;
            
            int subtotal = hargabarang * jumlahbarang;
            
            if(subtotal > 100000){
                int hargakurang = subtotal * (diskon /100);
                totalhargabersih = subtotal - hargakurang;
            }else{
                totalhargabersih = subtotal;
            }
            
            
            String SQL = "INSERT INTO tb_product(id_barang,nama_barang,harga_barang,nama_kasir,jumlah_barang,tanggal_pembelian,diskon,total_harga)" 
                    + "VALUES('"+tfIDBarang.getText()
                    +"','"+tfNamaBarang.getText()
                    +"','"+tfHargaBarang.getText()
                    +"','"+tfNamaKasir.getText()
                    +"','"+tfJumlahBarang.getText()
                    +"','"+tanggal
                    + "','"+diskon
                    +"','"+totalhargabersih+"')";
                    
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this,"Data berhasil ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectedData();
            }
            else{
                JOptionPane.showMessageDialog(this,"Data gagal ditambahkan","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }
    }                                      

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        
        int baris = tbData.getSelectedRow();
        if (baris != -1){
            String idbarang = tbData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_product WHERE id_barang='"+idbarang+"'";
            int status = KoneksiDB.execute(SQL);
            if(status ==1){
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Pilih Baris Data Terlebih Dahulu", "EROR", JOptionPane.WARNING_MESSAGE);
        }   
    }//GEN-LAST:event_btAddActionPerformed

